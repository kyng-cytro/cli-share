export const showMessage = (
  message: string,
  type: "error" | "success" | "warn"
) => {
  if (type === "error")
    return console.log(chalk.bold.red(`\nERROR: ${message}`));

  if (type === "success")
    return console.log(chalk.bold.green(`\nSUCESS: ${message}`));

  if (type === "warn")
    return console.log(chalk.bold.yellow(`\nWARNING: ${message}`));
};

export const formatAsTable = (data: Object, options: Options) => {
  return chalk.gray(columnify(data, options));
};

export const bytesToGB = (bytes: number) => {
  const GB = bytes / (1024 * 1024 * 1024);
  return GB.toFixed(2) + " GB";
};

export const toDateString = (date: Date | string) => {
  return new Date(date).toDateString();
};
